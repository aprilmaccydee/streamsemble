    /// <summary>Mode-indexed table of the four 142-byte phase 1 replies.</summary>
    private static readonly byte[][] ReplyMessages = { Reply0, Reply1, Reply2, Reply3 };

    /// <summary>Fixed 12-byte fp-setup phase 2 reply header (FPLY 03 01 04 ...).</summary>
    private static readonly byte[] FpHeader =
    {
        0x46, 0x50, 0x4c, 0x59, 0x03, 0x01, 0x04, 0x00, 0x00, 0x00, 0x00, 0x14
    };

    /// <summary>
    /// Handles the first fp-setup message (mode-select, "seq 1"). The request is a
    /// 16-byte blob whose byte 14 selects one of four fixed reply tables.
    /// </summary>
    /// <param name="request">The raw 16-byte fp-setup phase 1 request body.</param>
    /// <returns>A newly allocated 142-byte reply.</returns>
    /// <exception cref="ArgumentException">
    /// Thrown if the request is not 16 bytes, is not FairPlay version 3, or selects a
    /// mode outside 0..3.
    /// </exception>
    public static byte[] HandleSetupPhase1(ReadOnlySpan<byte> request)
    {
        if (request.Length != Phase1RequestLength)
            throw new ArgumentException(
                $"fp-setup phase 1 request must be {Phase1RequestLength} bytes, got {request.Length}.",
                nameof(request));
        if (request[VersionOffset] != FairPlayVersion)
            throw new ArgumentException(
                $"Unsupported FairPlay version 0x{request[VersionOffset]:x2}; expected 0x{FairPlayVersion:x2}.",
                nameof(request));

        int mode = request[ModeOffset];
        if ((uint)mode >= (uint)ReplyMessages.Length)
            throw new ArgumentException(
                $"fp-setup phase 1 mode {mode} out of range 0..{ReplyMessages.Length - 1}.",
                nameof(request));

        return (byte[])ReplyMessages[mode].Clone();
    }

    /// <summary>
    /// Handles the second fp-setup message (key handshake, "seq 3"). The 164-byte
    /// request carries the FairPlay key material; the reply is the fixed 12-byte
    /// header followed by the last 20 bytes of the request (offset 144).
    /// </summary>
    /// <param name="request">The raw 164-byte fp-setup phase 2 request body.</param>
    /// <returns>A newly allocated 32-byte reply (12-byte header + 20-byte echoed suffix).</returns>
    /// <exception cref="ArgumentException">
    /// Thrown if the request is not 164 bytes or is not FairPlay version 3.
    /// </exception>
    public static byte[] HandleSetupPhase2(ReadOnlySpan<byte> request)
    {
        if (request.Length != Phase2RequestLength)
            throw new ArgumentException(
                $"fp-setup phase 2 request must be {Phase2RequestLength} bytes, got {request.Length}.",
                nameof(request));
        if (request[VersionOffset] != FairPlayVersion)
            throw new ArgumentException(
                $"Unsupported FairPlay version 0x{request[VersionOffset]:x2}; expected 0x{FairPlayVersion:x2}.",
                nameof(request));

        var reply = new byte[HeaderLength + SuffixLength];
        FpHeader.AsSpan(0, HeaderLength).CopyTo(reply);
        // Echo the last 20 bytes of the request (offset 144 for a 164-byte request).
        request.Slice(request.Length - SuffixLength, SuffixLength).CopyTo(reply.AsSpan(HeaderLength));
        return reply;
    }
